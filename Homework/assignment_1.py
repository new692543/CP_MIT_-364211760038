x = 100  # ประกาศตัวแปร x มีค่าเท่ากับ 100
print(x) # แสดงผลค่าตัวแปร x
print(type(x)) # แสดงชนิดข้อมูลของตัวแปร x

y =  200 # ประกาศตัวแปร  y มีค่าเท่ากับ 200
print(y) # แสดงผลค่าตัวแปร y
print(type(y)) # แสดงชนิดข้อมูลของตัวแปร y

print('x is ',x,'and y is ',y,'') # แสดงผลค่าตัวแปร x และ y โดยมีข้อความดังนี้  "x is 100 and y is 200"

z = x+y  # หาผลรวมของตัวแปร x และ y และเก็บไว้ในตัวแปร z
